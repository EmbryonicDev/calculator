function add(a, b) {
  let answer = a + b;
  return answer;
}

function subtract(a, b) {
  let answer = a - b;
  return answer;
}

function multiply(a, b) {
  let answer = a * b;
  return answer;
}

function devide(a, b) {
  let answer = a / b;
  return answer;
}

// ------------------------
console.log("add");
console.log(add(9,9));

console.log("subtract");
console.log(subtract(19,9));

console.log("multiply");
console.log(multiply(9,9));

console.log("devide");
console.log(devide(27, 3));
